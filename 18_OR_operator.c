#include <stdio.h>

/* 
    - LOGICAL OPERATOR - OR (||) 
        - || (OR) checks if at least one of given conditions is true and will execute if even one given condition is true 
*/

int main() 
{
    double cc = 150;

    if (cc == 100 || cc == 150)
    {
        printf("Your vehicle is low powered vehicle");
    }
    
    return 0;
}